let weather = 'storm'

switch (weather) {
    case 'day':
        console.log('Nice day to go outside')
        break
    case 'hot':
        console.log('Drink a lot of water')
        break
    case 'rain':
        console.log('Bring an umbrella')
        break
    case 'storm':
        console.log('Stay at home!')
        break
    default:
        console.log('Interesting')
}